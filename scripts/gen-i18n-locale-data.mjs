/**
 * Generates scripts/i18n-locale-data.json for build-i18n-locales.mjs
 * Run: node scripts/gen-i18n-locale-data.mjs
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const outPath = path.join(__dirname, 'i18n-locale-data.json')

const L = (en, pl) => ({ en, pl })

/** @param {Array<[string, string, string]>} pairs */
function buildNs(pairs) {
  return Object.fromEntries(pairs.map(([k, en, pl]) => [k, L(en, pl)]))
}

/** @param {Record<string, unknown>} obj */
function countLeaves(obj) {
  let n = 0
  for (const v of Object.values(obj)) {
    if (v && typeof v === 'object' && 'en' in v && 'pl' in v) n++
    else if (v && typeof v === 'object') n += countLeaves(v)
  }
  return n
}

